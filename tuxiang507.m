clear;clc;I=imread('C:\Users\嗜血\Desktop\J1.jpg');
I1=rgb2gray(I);I1=imnoise(I1,'gaussian',0.02);
subplot(2,2,1),imshow(I1);title('噪声图像');
FFT=fft2(I1);myangle=angle(FFT);
FS=abs(fftshift(FFT));
S=log(1+abs(FS));
subplot(2,2,2),imshow(S,[]);title('傅里叶变换后的幅度图');
[m,n]=size(FS);FS(1:(m+1)/2-75,:)=0;FS((m+1)/2+75:m,:)=0;
FS((m+1)/2-75:(m+1)/2+75,1:n/2-75)=0;
FS((m+1)/2-75:(m+1)/2+75,n/2+75:n)=0;
SS=log(1+abs(FS));
subplot(2,2,3),imshow(SS,[]);title('去除外围幅值后幅度图');
aaa=ifftshift(FS);
bbb=aaa.*cos(myangle)+aaa.*sin(myangle).*1i;
fr=abs(ifft2(bbb));
ret=im2uint8(mat2gray(fr));
subplot(2,2,4),imshow(ret);title('去除高频成分后的图像');